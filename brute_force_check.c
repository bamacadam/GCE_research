/*
 these functions find the initial set of solutions
 */


void count_solutions(int flag[2187][4], int len[4])
{
  int i, j;
  for (i=0; i<4; i++)
  {
    len[i] = 0;
    for (j=0; j<2187; j++)
    {
      if (flag[j][i] == 1)
      {
        len[i]++;
      }
    }
  }
}


